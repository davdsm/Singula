export const useSendMail = () => {
  const [Sent, setSent] = useState<boolean>(false);
  const [Loading, setLoading] = useState<boolean>(false);

  const sendMail = async (
    name: string,
    contact: string,
    email: string,
    message: string
  ) => {
    if (!Sent) {
      setSent(false);
      setLoading(true);
      await goMail(name, contact, message, email);
      setTimeout(() => {
        setLoading(false);
        setSent(true);
      }, 2000);
    }
  };

  return { sendMail, Sent, Loading };
};
